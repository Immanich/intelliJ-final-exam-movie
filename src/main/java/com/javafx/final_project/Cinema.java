package com.javafx.final_project;

public class Cinema {
    private String name;
    private String title;
    private int qty;
    private float price;

    public Cinema(String name, String title, int qty, float price) {
        this.name = name;
        this.title = title;
        this.qty = qty;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
}